package day03;

public class DatatypeDemo {

	public static void main(String[] args) {
	
		final int a = 10;
		System.out.println("a = " + a);
		
		float b = 10.9f;
		System.out.println("b = "+ b);
		
		double c = 10.999354337839837d;
		System.out.println("c = " + c);
		
		long d = 9876543210l;
		System.out.println("d = "+ d);
		
		char e = 'h';
		System.out.println("e = "+ e);
		
	}

}
